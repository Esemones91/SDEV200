public class DemoArray3 
{
    public static void main(String[] args) 
    {
        double[] salaries = {12.25, 13.55, 14.25, 16.85};
        int x;
        
        System.out.println("Salaries one by one are: ");
        
        for(x = 0; x < salaries.length; ++x)
        {
            System.out.println(salaries[x]);
        }
    }
}
